//: ## Enumerations
//: Enums can represent any value in a finite set of related values.  Here are some examples ...
//: Primary colors
enum PrimaryColor {
    case red
    case blue
    case yellow
}
//: Baseball teams of the American League West
enum AmericanLeagueWest {
    case athletics
    case astros
    case angels
    case mariners
    case rangers
}
